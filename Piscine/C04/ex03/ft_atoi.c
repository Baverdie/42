int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	nb;

	neg = 0;
	i = 0;
	nb = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	if (neg % 2 == 1)
		return (-nb);
	return (nb);
}

